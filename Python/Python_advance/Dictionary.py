# This is a dictionary
x = {'Name': 'Anushanga', 'Age': 36, 'City': 'Colombo'}

# Get the keys
for i in x:
    print(i)

# Get the values
for i in x.values():
    print(i)

# Get the keys and values
for i, j in x.items():
    print(i, j)

# Get the range wise values
for i in range(2, 11):
    print(i)